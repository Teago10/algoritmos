using System;

class DeclaracaoVariaveis
{
	static void Main()
	{
		int a;
		// Inicialização da Variável
		a = 22;
		Console.WriteLine(a);
		// Reatribuição de valor na Váriavel
		a = 56;
		Console.WriteLine(a);
		// Outras formas de declarar uma váriavel
		
		//Declaração e inicializaçã da Váriavel
		int b = 925;
		Console.WriteLine(b);
		
		int y = 10, x = 20;
		int z = y + x;
		Console.WriteLine(z);
	}
}